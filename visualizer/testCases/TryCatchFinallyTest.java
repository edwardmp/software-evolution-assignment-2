public class TryCatchFinallyTest {
	public void tryCatchFinallyTest() {
		try {
			System.out.println("test");
		}
		catch(Exception e) {}
		finally {
			System.out.println("test");
			System.out.println("test");
			System.out.println("test");
			System.out.println("test");
			System.out.println("test");
			System.out.println("test");
		}
	}
}